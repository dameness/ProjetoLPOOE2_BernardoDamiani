    public void setDisciplina(Disciplina disciplina) {
        lblCodigo.setText("Código: "+ disciplina.getId());
        cmbProfessor.getModel().setSelectedItem((Professor) disciplina.getProfessor());
        txtCargaHoraria.setText(disciplina.getCarga_horaria()+ "");
        this.disciplina = disciplina;
    }
    
    public void listarProfessores(){
        cmbProfessor.removeAllItems();
        PersistenciaJPA jpa = new PersistenciaJPA();
        List<Professor> lista = jpa.getProfessores();
        
        for(Professor p : lista){
            cmbProfessor.addItem(p);
        }
        
        jpa.fecharConexao();
                
    }


