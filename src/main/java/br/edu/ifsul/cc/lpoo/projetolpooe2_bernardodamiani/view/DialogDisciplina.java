    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        PersistenciaJPA jpa = new PersistenciaJPA();

        if (disciplina == null) {
            try {
                this.disciplina = new Disciplina();
                disciplina.setCarga_horaria(Integer.parseInt(txtCargaHoraria.getText()));
                disciplina.setProfessor((Professor)cmbProfessor.getSelectedItem());
              
                jpa.persist(disciplina);

                JOptionPane.showMessageDialog(rootPane, "Disciplina persistida!");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar disciplina!");
                System.out.println(e);
            }

        } else {
            try {
                Disciplina editar = (Disciplina) jpa.find(Disciplina.class, disciplina.getId());
                

                editar.setCarga_horaria(Integer.parseInt(txtCargaHoraria.getText()));
                editar.setProfessor((Professor)cmbProfessor.getSelectedItem());
                
              
                jpa.persist(editar);

                JOptionPane.showMessageDialog(rootPane, "Disciplina persistida!");

            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(rootPane, "Erro ao editar disciplina!");
            }

        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

