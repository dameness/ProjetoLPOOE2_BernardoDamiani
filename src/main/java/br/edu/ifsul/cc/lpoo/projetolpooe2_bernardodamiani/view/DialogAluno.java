    public void listarDisciplinas(){
        cmbDisciplina.removeAllItems();
        PersistenciaJPA jpa = new PersistenciaJPA();
        List<Disciplina> lista = jpa.getDisciplinas();
        
        for(Disciplina d : lista){
            cmbDisciplina.addItem(d);
        }
        
        jpa.fecharConexao();
                
    }


