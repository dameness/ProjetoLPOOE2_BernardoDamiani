    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        PersistenciaJPA jpa = new PersistenciaJPA();

        if (aluno == null) {
            try {
                this.aluno = new Aluno();
                aluno.setNome(txtNome.getText());
                String telefone = txtTelefone.getText();
                for(int i=0; i<telefone.length(); i++){
                    if(!isDigit(telefone.charAt(i))){
                        throw new Exception("Telefone inválido!");
                    }
                        
                }
                aluno.setTelefone(telefone);
                aluno.setDisciplina((Disciplina)cmbDisciplina.getSelectedItem());
              
                jpa.persist(aluno);

                JOptionPane.showMessageDialog(rootPane, "Aluno persistido!");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Erro ao cadastrar aluno!");
                System.out.println(e);
            }

        } else {
            try {
                Aluno editar = (Aluno) jpa.find(Aluno.class, aluno.getId());
                

                editar.setNome(txtNome.getText());
                String telefone = txtTelefone.getText();
                for(int i=0; i<telefone.length(); i++){
                    if(!isDigit(telefone.charAt(i))){
                        throw new Exception("Telefone inválido!");
                    }
                        
                }
                editar.setTelefone(telefone);
                editar.setDisciplina((Disciplina)cmbDisciplina.getSelectedItem());
                
              
                jpa.persist(editar);

                JOptionPane.showMessageDialog(rootPane, "Aluno persistido!");

            } catch (Exception e) {
                System.out.println(e);
                JOptionPane.showMessageDialog(rootPane, "Erro ao editar disciplino!");
            }

        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnConfirmarActionPerformed

